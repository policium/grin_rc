static bool is_left_touch[4] = { false, false, false, false };
static int8_t left_touch_start = -1;
static uint32_t left_touch_time = 0;
static uint16_t left_touch_cnt[4] = { 0, 0, 0, 0 };
static bool is_left_tapped = false;
static uint32_t last_left_time = 0;
static int16_t last_left_value = 0;

static bool is_right_touch[4] = { false, false, false, false };
static int8_t right_touch_start = -1;
static uint32_t right_touch_time = 0;
static uint16_t right_touch_cnt[4] = { 0, 0, 0, 0 };
static bool is_right_tapped = false;
static uint32_t last_right_time = 0;
static int16_t last_right_value = 0;

static bool is_tp1_touch = false;
static uint32_t tp1_touch_time = 0;
static uint16_t tp1_touch_cnt = 0;
static bool is_tp1_tapped = false;

static int16_t read_left_value(void) {
    uint8_t cnt = 0;
    int16_t val = 0;
    if (is_left_touch[0]) {
        cnt++;
        val += 100;
    }
    if (is_left_touch[1]) {
        cnt++;
        val += 75;
    }
    if (is_left_touch[2]) {
        cnt++;
        val += 50;
    }
    if (is_left_touch[3]) {
        cnt++;
        val += 25;
    }
    if (cnt == 0) return 0;
    return val / cnt;
}

static int16_t read_right_value(void) {
    uint8_t cnt = 0;
    int16_t val = 0;
    if (is_right_touch[0]) {
        cnt++;
        val += 100;
    }
    if (is_right_touch[1]) {
        cnt++;
        val += 75;
    }
    if (is_right_touch[2]) {
        cnt++;
        val += 50;
    }
    if (is_right_touch[3]) {
        cnt++;
        val += 25;
    }
    if (cnt == 0) return 0;
    return val / cnt;
}

static bool count_touch(bool read, bool *pcurrent, uint16_t *pcnt, int16_t threshold) {
    // count up & down
    if (read && *pcnt < (threshold * 2)) {
        *pcnt += 1;
    } else if (!read && *pcnt > 0) {
        *pcnt -= 1;
    }
    // decide
    if (*pcurrent != (*pcnt > threshold)) {
        *pcurrent = (*pcnt > threshold);
        return true;
    }
    return false;
}

static void check_touch(void) {
    // Left
    if (count_touch(!readPin(TP_L1), &is_left_touch[0], &left_touch_cnt[0], 400) |
        count_touch(!readPin(TP_L2), &is_left_touch[1], &left_touch_cnt[1], 400) |
        count_touch(!readPin(TP_L3), &is_left_touch[2], &left_touch_cnt[2], 400) |
        count_touch(!readPin(TP_L4), &is_left_touch[3], &left_touch_cnt[3], 400)) {
        if (left_touch_start == -1 && (is_left_touch[0] || is_left_touch[1] || is_left_touch[2] || is_left_touch[3])) {
            // touch
            dprint("touch left\n");
            left_touch_time = last_left_time = timer_read32();
            for (int i = 0; i < 4; i++) {
                if (is_left_touch[i]) {
                    left_touch_start = i;
                }
            }
            last_left_value = read_left_value();
            //last_left_time = timer_read32();
            is_left_tapped = false;
        } else if (left_touch_start != -1 && !(is_left_touch[0] || is_left_touch[1] || is_left_touch[2] || is_left_touch[3])) {
            // untouch
            if (timer_read32() - left_touch_time <= 300) {
                // detect tap
                is_left_tapped = true;
            }
            last_left_time = timer_read32();
        } else {
            // slide
            uint32_t time = timer_read32();
            int16_t left_value = read_left_value();
            if (timer_read32() - left_touch_time > 500) {
                on_slide_left(left_value, left_value - last_left_value, time - last_left_time);
                is_left_tapped = false;
            }
            last_left_value = left_value;
            last_left_time = time;
            //is_left_tapped = true;
        }
    } else {
        if (left_touch_start != -1 && (is_left_touch[0] || is_left_touch[1] || is_left_touch[2] || is_left_touch[3])) {
            uint32_t time = timer_read32();
            if (!is_left_tapped && time - last_left_time > 300) {
                // slide
                int16_t left_value = read_left_value();
                on_slide_left(left_value, 0, time - last_left_time);
                last_left_time = time;
                is_left_tapped = false;
            }
        } else if (left_touch_start != -1 && !(is_left_touch[0] || is_left_touch[1] || is_left_touch[2] || is_left_touch[3])) {
            if (timer_read32() - last_left_time > 300) {
                // untouch
                dprint("untouch left\n");
                if (is_left_tapped) {
                    on_tapped_left();
                }
                left_touch_start = -1;
                last_left_value = 0;
            }
        }
    }
    // Right
    if (count_touch(!readPin(TP_R1), &is_right_touch[0], &right_touch_cnt[0], 400) |
        count_touch(!readPin(TP_R2), &is_right_touch[1], &right_touch_cnt[1], 400) |
        count_touch(!readPin(TP_R3), &is_right_touch[2], &right_touch_cnt[2], 400) |
        count_touch(!readPin(TP_R4), &is_right_touch[3], &right_touch_cnt[3], 400)) {
        if (right_touch_start == -1 && (is_right_touch[0] || is_right_touch[1] || is_right_touch[2] || is_right_touch[3])) {
            // touch
            dprint("touch right\n");
            right_touch_time = last_right_time = timer_read32();
            for (int i = 0; i < 4; i++) {
                if (is_right_touch[i]) {
                    right_touch_start = i;
                }
            }
            last_right_value = read_right_value();
            //last_right_time = timer_read32();
            is_right_tapped = false;
        } else if (right_touch_start != -1 && !(is_right_touch[0] || is_right_touch[1] || is_right_touch[2] || is_right_touch[3])) {
            // untouch
            if (timer_read32() - right_touch_time <= 300) {
                // detect tap
                is_right_tapped = true;
            }
            last_right_time = timer_read32();
        } else {
            // slide
            uint32_t time = timer_read32();
            int16_t right_value = read_right_value();
            if (timer_read32() - right_touch_time > 500) {
                on_slide_right(right_value, right_value - last_right_value, time - last_right_time);
                is_right_tapped = false;
            }
            last_right_value = right_value;
            last_right_time = time;
            //is_right_tapped = true;
        }
    } else {
        if (right_touch_start != -1 && (is_right_touch[0] || is_right_touch[1] || is_right_touch[2] || is_right_touch[3])) {
            uint32_t time = timer_read32();
            if (!is_right_tapped && time - last_right_time > 300) {
                // slide
                int16_t right_value = read_right_value();
                on_slide_right(right_value, 0, time - last_right_time);
                last_right_time = time;
                is_right_tapped = false;
            }
        } else if (right_touch_start != -1 && !(is_right_touch[0] || is_right_touch[1] || is_right_touch[2] || is_right_touch[3])) {
            if (timer_read32() - last_right_time > 300) {
                // untouch
                dprint("untouch right\n");
                if (is_right_tapped) {
                    on_tapped_right();
                }
                right_touch_start = -1;
                last_right_value = 0;
            }
        }
    }
    // TP
    if (count_touch(!readPin(TP_1), &is_tp1_touch, &tp1_touch_cnt, 300)) {
        if (is_tp1_touch) {
            // touch
            tp1_touch_time = timer_read32();
            is_tp1_tapped = false;
        } else {
            // untouch
            if (!is_tp1_tapped && timer_read32() - tp1_touch_time < 800) {
                on_tapped();
            }
        }
    } else if (is_tp1_touch) {
        if (!is_tp1_tapped && timer_read32() - tp1_touch_time >= 800) {
            on_long_tapped();
            is_tp1_tapped = true;
        }
    }
}

