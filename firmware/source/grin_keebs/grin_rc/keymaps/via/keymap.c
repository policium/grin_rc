void matrix_scan_user(void) {
    check_touch();

    if (last_right_value == 0) {
        rgb_angle += right_speed;
    }

    while (rgb_angle < 0) {
        rgb_angle += 360;
    }
    while (rgb_angle >= 360) {
        rgb_angle -= 360;
    }
}

